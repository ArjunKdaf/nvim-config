vim.cmd([[ set encoding=utf8 ]]) -- mostrará em utf-8 apenas dentro do nvim
vim.cmd([[ set nu! ]]) -- linhas numeradas
vim.cmd([[ set mouse=a ]]) -- mouse em todos os modos
vim.cmd([[ set wildmenu ]]) -- :e e tab para menu de autocompletar vertical
vim.cmd([[ set confirm ]]) -- confirmar antes de sair 
vim.cmd([[ set incsearch ]]) -- buscar e destacar um termo no arquivo com / no MN
vim.cmd([[ set t_Co=256 ]]) -- trocar o padrão de 8 para 256 cores
vim.cmd([[ set shiftwidth=4 ]]) -- recuo do texto
vim.cmd([[ set softtabstop=4 ]]) -- tabulação
vim.cmd([[ set expandtab ]]) -- inserir espaços quando tab é pressionado
vim.cmd([[ set cursorline ]]) -- destacar a linha em que o cursor está
vim.cmd([[ syntax on ]]) -- syntax highlighting
vim.cmd([[ set bg=dark ]]) -- escolher cor do backgroud
vim.cmd([[ set termguicolors ]]) -- adicionar suporte a cores verdadeiras
vim.cmd([[ colorscheme dracula ]]) -- esquema de cores dracula

